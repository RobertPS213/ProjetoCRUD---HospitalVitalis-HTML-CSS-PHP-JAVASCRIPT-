                    <p>
                        Fundado em 1985 no coração de Brasília, o <strong>Hospital Vitalis</strong> nasceu do sonho de um grupo de médicos idealistas que acreditavam em uma medicina mais humana, acessível e de excelência. Naquela época, a capital do país carecia de instituições de saúde que atendessem à população com qualidade e respeito, especialmente nas regiões mais carentes da cidade. 
                    </p>
                    <p>
                        O nome "Vitalis", derivado do latim e significando "relativo à vida", foi escolhido para simbolizar o compromisso com a preservação, cuidado e valorização da vida em todas as suas fases. Começando com uma pequena equipe de 20 profissionais e apenas três especialidades médicas, o hospital logo ganhou reconhecimento pela dedicação e competência de seus colaboradores.
                    </p>
                    <p>
                        Ao longo das décadas, o Hospital Vitalis se expandiu, modernizou suas instalações e investiu fortemente em tecnologia e capacitação profissional. Hoje, conta com uma infraestrutura completa, 15 especialidades médicas, centro cirúrgico, unidade de terapia intensiva (UTI), pronto atendimento 24h e um laboratório próprio de diagnósticos. Tudo isso aliado a um atendimento acolhedor e personalizado.
                    </p>
                    <p>
                        Em 2002, o hospital deu um grande passo ao criar seu próprio programa de residência médica, contribuindo para a formação de novos profissionais comprometidos com a ética, a ciência e o cuidado integral ao paciente. Além disso, iniciou projetos sociais voltados à prevenção de doenças, saúde da mulher e campanhas de vacinação em comunidades carentes.
                    </p>
                    <p>
                        Durante a pandemia de COVID-19, o Hospital Vitalis foi um dos primeiros da região a montar alas de isolamento específicas, oferecer testagem em massa e garantir atendimento gratuito para pacientes em situação de vulnerabilidade. Sua atuação foi reconhecida por órgãos públicos e pela sociedade civil como essencial na luta contra o vírus.
                    </p>
                    <p>
                        Hoje, com mais de 500 colaboradores e milhares de vidas atendidas, o Hospital Vitalis segue firme em seu propósito: oferecer saúde com empatia, inovação e responsabilidade social. Mais que um hospital, o Vitalis é uma extensão da casa de cada paciente que entra por suas portas, sendo sinônimo de confiança, acolhimento e esperança.
                    </p>
